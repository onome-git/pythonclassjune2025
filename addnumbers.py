def calculate_sum(a, b):  # Function definition with parameters a and b
    return a + b          # Return the sum of a and b

num1 = int(input("Enter the first number: "))   # Convert input to integer
num2 = int(input("Enter the second number: "))  # Convert input to integer

print("The sum is:", calculate_sum(num1, num2))  # Call function and print result